#include <iostream>

using namespace std;

main()
{

	int a = 45, b;
	
	b = a + 30;
	
		cout << "a = " << a << '\n';
		cout << "b = " << b << '\n';
		cout << "______________________" << '\n';
		cout << "a = " << ++a << '\n';
		cout << "b = " << --b << '\n';
		cout << "______________________" << '\n';
		cout << "a = " << a++ << '\n';
		cout << "b = " << --b  << '\n';
		cout << "______________________" << '\n';
	
}